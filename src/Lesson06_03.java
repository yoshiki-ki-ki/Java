public class Lesson06_03 {
  public static void main(String[] args) {
    int var = 1;
    int var01 = 2;
    int var_01 = 3;
    int var$01 = 4;

    System.out.println(var);
    System.out.println(var01);
    System.out.println(var_01);
    System.out.println(var$01);
  }
}
